package org.example.fitness_server.model;

/**
 * Интерфейс для сущностей, представляющих пользователей в системе фитнес-клуба.
 * <p>
 * Этот интерфейс определяет базовые методы для работы с пользовательскими данными,
 * такими как имя, имя пользователя и пароль. Реализуется классами, представляющими
 * сущности с пользовательскими учётными данными, например, {@code Client} и {@code Trainer}.
 * </p>
 *
 * @author Милана
 * @version 1.0
 * @since 2025-04-29
 */

public interface UserEntity {

    /**
     * Возвращает имя пользователя.
     * <p>
     * Имя представляет собой полное имя человека (например, имя и фамилию),
     * используемое для отображения в системе.
     * </p>
     *
     * @return имя пользователя в виде строки
     */

    String getName();

    /**
     * Устанавливает имя пользователя.
     * <p>
     * Перед установкой имя должно быть очищено от лишних пробелов и валидировано
     * в соответствии с правилами системы.
     * </p>
     *
     * @param name новое имя пользователя
     */

    void setName(String name);

    /**
     * Возвращает имя пользователя для входа в систему.
     * <p>
     * Имя пользователя должно быть уникальным в пределах системы и используется
     * для аутентификации.
     * </p>
     *
     * @return имя пользователя в виде строки
     */

    String getUsername();

    /**
     * Устанавливает имя пользователя для входа в систему.
     * <p>
     * Перед установкой имя пользователя должно быть валидировано (например,
     * содержать от 3 до 20 символов, только буквы, цифры и подчёркивания).
     * </p>
     *
     * @param username новое имя пользователя
     */

    void setUsername(String username);

    /**
     * Возвращает пароль пользователя.
     * <p>
     * Пароль хранится в зашифрованном виде и используется для аутентификации.
     * </p>
     *
     * @return пароль в виде строки
     */

    String getPassword();

    /**
     * Устанавливает пароль пользователя.
     * <p>
     * Перед установкой пароль должен быть валидирован (например, минимум 6 символов)
     * и зашифрован с использованием {@code PasswordEncoder}.
     * </p>
     *
     * @param password новый пароль
     */

    void setPassword(String password);
}